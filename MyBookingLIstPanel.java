    public void showBookingInTable() {
        DefaultTableModel model = new DefaultTableModel(new String[]{"Flight Id", "Flight Title", "From", "To", "Date", "Time", "Description"}, 0);
        Connection conn = MySqlConfigration.connect();
        try {
            Statement statement = (Statement) conn.createStatement();
            ResultSet resultSet = statement.executeQuery("Select * From flight_user");

            while (resultSet.next()) {
                if (resultSet.getString("userId").equals(LoggedUserDetails.loggedUserId)) {
                    ResultSet flightDetails = statement.executeQuery("Select * From flight WHERE flightId = '" + resultSet.getString("flightId") + "'");
                    while (flightDetails.next()) {
                        model.addRow(new String[]{flightDetails.getString("flightId"), flightDetails.getString("flightTitle"), flightDetails.getString("flightFrom"), flightDetails.getString("flightTo"), flightDetails.getString("flightDate"), flightDetails.getString("flightTime"), flightDetails.getString("description")});
                    }
                    flightDetails.close();
                }

            }
            resultSet.close();
            MySqlConfigration.disconnect();
        } catch (SQLException ex) {
            System.err.println(ex);
        }

        myBookingList.setModel(model);

    }
