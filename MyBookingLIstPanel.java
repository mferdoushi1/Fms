    private void deleteFlightByFlightId(String selectedFlightId) {
        Connection connection = MySqlConfigration.connect();
        try {
            Statement statement = (Statement) connect().createStatement();
            statement.execute("DELETE FROM flight_user WHERE flightId = '" + selectedFlightId + "'");
            statement.close();
            MySqlConfigration.disconnect();
            JOptionPane.showMessageDialog(null, "Removed");
            showBookingInTable();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Somthing is worng");
            System.err.println(ex);
        }
    }
