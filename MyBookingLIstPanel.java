    private void myBookingListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_myBookingListMouseClicked
        int row = myBookingList.rowAtPoint(evt.getPoint());
        int col = myBookingList.columnAtPoint(evt.getPoint());
        if (row >= 0 && col >= 0) {
            String selectedFlightId = myBookingList.getValueAt(row, 0).toString();
            String[] choices = {"Delete"};
            String input = (String) JOptionPane.showInputDialog(null, "Please, make your choice", "Setting", JOptionPane.QUESTION_MESSAGE, null, choices, choices[0]);

            switch (input.toLowerCase()) {
                case "delete":
                    deleteFlightByFlightId(selectedFlightId);
                    break;

                default:
                    break;
            }
        }
    }//GEN-LAST:event_myBookingListMouseClicked
